const calcBucketIndex = (
  answers: string[],
  questions: any[],
  bucketCount: number
): number => {
  const totalScore = answers.reduce((sum, ans, i) => {
    const q = questions[i];
    const opt = q.options.find((o: any) => o.id === ans);
    return sum + (opt?.score ?? 0);
  }, 0);

  // ì§ˆë¬¸ 10ê°œ, ê° 0~3ì  â†’ ì´ì  0~30
  // ì˜ˆ: bucketCount = 8 â†’ 0~7
  const raw = Math.floor(totalScore / (31 / bucketCount)); // 31ì€ 0~30 ë²”ìœ„ ê¸¸ì´
  const index = Math.min(bucketCount - 1, Math.max(0, raw));
  return index;
};

export const calcFortuneResultIndex = (answers: string[]): number => {
  const questions = serviceData.fortune.questions;
  return calcBucketIndex(answers, questions, serviceData.fortune.results.length);
};

